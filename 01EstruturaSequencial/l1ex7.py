#7. Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário.
a = float(input("Insira a medida do primeiro lado do quadrado: "))
b = float(input("Insira a medida do segundo lado do quadrado: "))
print(f'A área do quadrado é: {a*b:.2f}')
print(f'A dobro da área é: {a*b*2:.2f}')